    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDuringWork() {
        return duringWork;
    }

    public void setDuringWork(String duringWork) {
        this.duringWork = duringWork;
    }

    public String getJobDay() {
        return jobDay;
    }

    public void setJobDay(String jobDay) {
        this.jobDay = jobDay;
    }

    public String getJobKind() {
        return jobKind;
    }

    public void setJobKind(String jobKind) {
        this.jobKind = jobKind;
    }

    public String getRecruitmentContents() {
        return recruitmentContents;
    }

    public void setRecruitmentContents(String recruitmentContents) {
        this.recruitmentContents = recruitmentContents;
    }

